public class PreIncrementExFive
{
	public static void main(String[]args)
	{
	System.out.println("Start of Main Method");
	//float k=45.6; 45.6 is considered as double datatype
	float k=45.6f;
	System.out.println("The initial value of k is :"+k);
	++k;
	System.out.println("The final value of  is "+k);
	System.out.println("End of Main Method");
	}
}