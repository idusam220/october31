public class PreIncrementExOne
{
	public static void main(String[]args)
	{
	System.out.println("Start of Main Method");
	byte variableOne=23;
	System.out.println("The initial value of Variable one is :"+variableOne);
	byte variableTwo=++variableOne;
	System.out.println("The value of variable two is :"+variableTwo);
	System.out.println("The final value of variable one is :"+variableOne);
	System.out.println("End of Main Method");
	}
}