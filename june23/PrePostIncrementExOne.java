public class PrePostIncrementExOne
{
	public static void main(String[]args)
	{
		double d=10.5;
		++d;
		System.out.println(d++);
		d++;
		System.out.println(++d);
		++d;
		System.out.println(++d);
		double b=34.2;
		b++;
		System.out.println(b++);
		System.out.println(++b);
		System.out.println(++d);
		System.out.println(+b);
		System.out.println("End of Main Method");

	}
}